module.exports.showSidePane = function( $pane ){
    if ( !$pane ) return false;

    $(".overlay").show();

    // 一旦消す
    $(".sidePane")
        .removeClass("isVisible")
        .addClass("isHidden");

    // 出す
    $pane
        .addClass("isVisible");

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

module.exports.hideSidePane = function( $pane ){
    if ( !$pane ) return false;

    $(".overlay").hide();

    // 消す
    $pane
        .removeClass("isVisible")
        .addClass("isHidden");

}

