$(function() {

    /////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 一覧画面
    /////////////////////////////////////////////////////////////////////////////////////////////////////////

    // ---------------------------------------------------------------------------------
    // 追加ボタン
    // ---------------------------------------------------------------------------------

    var $addButton = $(".actionAdd");

    $addButton.on("click", function(){
        var $currentForm = $(this).parents("form");

        console.log( $currentForm )
        $currentForm.submit();
    });

    // ---------------------------------------------------------------------------------
    // 編集ボタン
    // ---------------------------------------------------------------------------------

    var $editButton = $(".actionEdit");

    $editButton.on("click", function(){
        var $currentForm = $(this).parents("form");
        var $method = $("input[name=_method]", $currentForm);
        $method.val("PUT");
        $currentForm.submit();
    });

    // ---------------------------------------------------------------------------------
    // 削除ボタン
    // ---------------------------------------------------------------------------------

    var $deleteButton = $(".actionRemove");

    $deleteButton.on("click", function(){
        var $currentForm = $(this).parents("form");
        var $method = $("input[name=_method]", $currentForm);
        $method.val("DELETE");
        $currentForm.submit();
    });

});
