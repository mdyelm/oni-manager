////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
var config = require("config");
var express = require('express');
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
var viewController = require('../controllers/menus/menusViewController');
var apiController = require('../controllers/menus/menusApiController');
var recycleUploader = require('../controllers/_modules/recycleUploader');
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

var router = express.Router();

router.use(function checkAuth( req, res, next ){
    if (!req.user) { res.redirect('/login'); return } // セッション確認
    next();
})

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// View
router.get('/', viewController.index); // 一覧表示

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// API

router.post("/", apiController.add ); // メニュー項目の追加
router.put('/:id', apiController.edit ); // メニューの編集
router.delete('/:id', apiController.remove ); // ピックアップ枠の削除

router.put("/position/save", apiController.savePosition ); // 並び順保存

// アップローダー設定

// 背景画像用
router.get('/images/buttonBackgrounds', recycleUploader.getFileList );
router.post('/images/buttonBackgrounds', recycleUploader.save );
router.delete('/images/buttonBackgrounds/:fileName', recycleUploader.remove );

// アイコン画像用
router.get('/images/buttonIcons', recycleUploader.getFileList );
router.post('/images/buttonIcons', recycleUploader.save );
router.delete('/images/buttonIcons/:fileName', recycleUploader.remove );

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

module.exports = router;
