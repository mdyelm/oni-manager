var config = require('config');
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
*   knexの初期化を行いDB操作が行える状態で提供する
*   @class knex
*   @module mysqlConnection
*   @extends {knex}
*/

